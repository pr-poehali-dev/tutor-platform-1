-- Применяем дисклеймеры ко всем существующим курсам по их subject.
-- Это аналог Python-фильтра apply_compliance, выполненный на стороне БД, чтобы
-- не нужно было перегенерировать все 48 курсов через функцию.

-- Базовый дисклеймер 152-ФЗ — для всех курсов
UPDATE course_curricula
SET disclaimers = jsonb_build_array(
    'При прохождении курса соблюдается 152-ФЗ «О персональных данных». Ваши данные не передаются третьим лицам без вашего согласия.'
);

