// Дополнительные «ценностные» опции для КАЖДОГО курса.
// Цель — чтобы любой курс был глубоким и полезным, а покупатель ясно понимал,
// что именно он получает за деньги. Данные генерируются на основе предмета,
// типа курса (экзамен/детский/взрослый) и формата.

import { Course } from "./coursesData";

export interface ValueItem {
  icon: string;
  title: string;
  text: string;
}

export interface FaqItem {
  q: string;
  a: string;
}

const EXAM_GRADES = ["ege", "oge"];
const KID_GRADES = ["1-4"];

function isExam(course: Course): boolean {
  return EXAM_GRADES.includes(course.grade);
}

function isKids(course: Course): boolean {
  return KID_GRADES.includes(course.grade);
}

function isAdult(course: Course): boolean {
  return course.grade === "adult";
}

