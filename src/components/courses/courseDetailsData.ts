// ВАЖНО: отзывы обезличены (соблюдение 152-ФЗ «О персональных данных»),
// инициалы и города — обобщённые/условные, не относятся к реальным лицам.
// Прогнозы баллов и результатов носят ориентировочный характер.

export interface ModuleLesson {
  num: number;
  title: string;
  duration: string;
  topics: string[];
}

export interface CourseModule {
  id: number;
  title: string;
  lessons: ModuleLesson[];
}

export interface AnonymousReview {
  initials: string;
  city: string;
  grade: string;
  text: string;
  rating: number;
  date: string;
}

export interface CourseDetail {
  outcomes: string[];
  forWhom: string[];
  modules: CourseModule[];
  reviews: AnonymousReview[];
}

// Универсальный шаблон для всех курсов (генерируется на основе предмета)
export function getCourseDetail(course: {
  id: number;
  subject: string;
  title: string;
  grade: string;
  lessons: number;
  tags: string[];
}): CourseDetail {
  // Базовые модули — делим уроки на 4 логических блока
  const lessonsPerModule = Math.ceil(course.lessons / 4);
  const moduleTitles = getModuleTitles(course.subject, course.tags);
  const modules: CourseModule[] = moduleTitles.map((mt, i) => ({
    id: i + 1,
    title: mt.title,
    lessons: Array.from({ length: Math.min(lessonsPerModule, 6) }, (_, j) => ({
      num: j + 1,
      title: mt.lessons[j] || `Урок ${j + 1}: практика темы`,
      duration: `${15 + (j % 3) * 5} мин`,
      topics: mt.topics.slice(0, 3),
    })),
  }));

  return {
    outcomes: getOutcomes(course.subject, course.grade),
    forWhom: getForWhom(course.subject, course.grade),
    modules,
    reviews: getReviews(course.subject),
  };
}

