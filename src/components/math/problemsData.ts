export function filterProblems(
  problems: Problem[],
  topic: Topic | "all",
  difficulty: Difficulty | "all",
): Problem[] {
  return problems.filter((p) => {
    if (topic !== "all" && p.topic !== topic) return false;
    if (difficulty !== "all" && p.difficulty !== difficulty) return false;
    return true;
  });
}
