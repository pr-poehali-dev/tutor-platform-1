export function getLesson(id: string): DrawLesson | undefined {
  return DRAW_LESSONS.find((l) => l.id === id);
}

export function filterLessons(category: DrawCategory | "all", level: DrawLevel | "all"): DrawLesson[] {
  return DRAW_LESSONS.filter((l) => {
    if (category !== "all" && l.category !== category) return false;
    if (level !== "all" && l.level !== level) return false;
    return true;
  });
}