/**
 * Модуль РИСОВАШКА — данные уроков рисования.
 * По методике академической школы: от простых форм к деталям.
 * Каждый урок — последовательность шагов с инструкциями для голоса ИИ-наставника.
 */

export type DrawLevel = "beginner" | "easy" | "medium" | "hard";
export type DrawCategory = "shapes" | "animals" | "people" | "nature" | "objects" | "fantasy";

/**
 * SVG-элемент трафарета. Координаты — в виртуальной системе 600×450.
 * DrawCanvas масштабирует под реальный размер холста автоматически.
 */
export type TemplateShape =
  | { kind: "circle"; cx: number; cy: number; r: number }
  | { kind: "ellipse"; cx: number; cy: number; rx: number; ry: number }
  | { kind: "rect"; x: number; y: number; w: number; h: number }
  | { kind: "line"; x1: number; y1: number; x2: number; y2: number }
  | { kind: "polyline"; points: [number, number][] }
  | { kind: "polygon"; points: [number, number][] }
  | { kind: "arc"; cx: number; cy: number; r: number; start: number; end: number };

/** Размер виртуальной системы координат трафарета. */
export const TEMPLATE_VIEW_W = 600;
export const TEMPLATE_VIEW_H = 450;

export interface DrawStep {
  /** Название шага: «Шаг 1. Овал» */
  title: string;
  /** Что сказать вслух — короткая инструкция */
  voice: string;
  /** Подсказка/совет внизу шага */
  tip?: string;
  /** Какой инструмент рекомендовать выбрать */
  suggestTool?: "pencil" | "brush" | "marker" | "eraser";
  /** Какой цвет рекомендовать */
  suggestColor?: string;
  /** Рекомендуемая толщина линии */
  suggestSize?: number;
  /** Длительность шага в секундах для оценки */
  estSec?: number;
  /**
   * Трафарет-образец «делай как я».
   * Полупрозрачный контур поверх холста — ребёнок ведёт по нему.
   * На beginner-уроках включается автоматически и работает как обводка.
   */
  template?: TemplateShape[];
}

export interface DrawLesson {
  id: string;
  title: string;
  emoji: string;
  category: DrawCategory;
  level: DrawLevel;
  ageRange: string; // "3–6 лет"
  durationMin: number;
  description: string;
  /** Что научится делать ребёнок после урока */
  skills: string[];
  /** Какой художник/стиль вдохновение (для культурного контекста) */
  inspiration?: string;
  /** Главный приём этого урока */
  keyTechnique: string;
  color: string; // tailwind gradient
  steps: DrawStep[];
}

