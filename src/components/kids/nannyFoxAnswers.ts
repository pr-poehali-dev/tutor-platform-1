/** Нормализация строки для сравнения */
function normalize(s: string): string {
  return s
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[«»"„"'`]/g, "")
    .replace(/[?!.,;:]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

/** Найти готовый ответ по тексту вопроса (с учётом нормализации). Возвращает null, если нет точного совпадения. */
export function findReadyAnswer(question: string): string | null {
  const target = normalize(question);
  if (!target) return null;
  for (const item of READY_ANSWERS) {
    if (normalize(item.question) === target) return item.answer;
  }
  return null;
}
