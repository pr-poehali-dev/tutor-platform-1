// Каждый урок открывает чат с наставником (с голосом) по конкретной теме.

export interface CourseLesson {
  id: string;
  title: string;
  goal: string; // чему научится ученик
  starter: string; // первый вопрос/запрос наставнику при старте урока
}

export interface CourseModule {
  id: string;
  title: string;
  emoji: string;
  lessons: CourseLesson[];
}

export interface SuperCourse {
  id: string;
  subject: string; // соответствует предмету и наставнику
  teacherId: string; // alex / dmitry
  title: string;
  emoji: string;
  accent: string;
  tagline: string;
  level: string;
  modules: CourseModule[];
}

