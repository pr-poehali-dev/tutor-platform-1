import type { LessonNotes } from "./lessonTypes";

export type { WorkedExample, PracticeTask, LessonNotes } from "./lessonTypes";

export interface CourseLesson {
  id: string;
  title: string;
  goal: string; // чему научится ученик
  starter: string; // первый вопрос/запрос наставнику при старте урока
  notes?: LessonNotes; // выверенный учебный конспект по теме
}

export interface CourseModule {
  id: string;
  title: string;
  emoji: string;
  lessons: CourseLesson[];
  /** Целевой класс раздела: "7"…"11" или "ege". Для индивидуальной программы по уровню. */
  grade?: GradeLevel;
}

export type GradeLevel = "7" | "8" | "9" | "10" | "11" | "ege";

/** Порядок уровней для фильтрации «мой класс и ниже». */
export const GRADE_ORDER: GradeLevel[] = ["7", "8", "9", "10", "11", "ege"];

/** Показывать ли раздел ученику выбранного класса: его уровень и всё, что проходят раньше. */
export function moduleMatchesGrade(moduleGrade: GradeLevel | undefined, studentGrade: string): boolean {
  if (!studentGrade || !moduleGrade) return true; // класс не выбран или у раздела нет метки — показываем всё
  const si = GRADE_ORDER.indexOf(studentGrade as GradeLevel);
  const mi = GRADE_ORDER.indexOf(moduleGrade);
  if (si < 0 || mi < 0) return true;
  // Ученик видит темы своего уровня и всех предыдущих; выпускник (ege) видит всё.
  return mi <= si;
}

export interface SuperCourse {
  id: string;
  courseId: number; // числовой ID для оплаты через кассу (course_purchases)
  subject: string; // соответствует предмету и наставнику
  teacherId: string; // alex / dmitry
  title: string;
  emoji: string;
  accent: string;
  tagline: string;
  level: string;
  price: number; // цена полного доступа, ₽
  oldPrice: number; // зачёркнутая цена для показа выгоды, ₽
  modules: CourseModule[];
}

/** Доступен ли урок бесплатно: первый урок первого раздела — демо. */
export function isLessonFree(course: SuperCourse, lessonId: string): boolean {
  return course.modules[0]?.lessons[0]?.id === lessonId;
}

