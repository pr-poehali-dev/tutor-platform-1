export interface CourseLesson {
  id: string;
  title: string;
  goal: string; // чему научится ученик
  starter: string; // первый вопрос/запрос наставнику при старте урока
}

export interface CourseModule {
  id: string;
  title: string;
  emoji: string;
  lessons: CourseLesson[];
}

export interface SuperCourse {
  id: string;
  courseId: number; // числовой ID для оплаты через кассу (course_purchases)
  subject: string; // соответствует предмету и наставнику
  teacherId: string; // alex / dmitry
  title: string;
  emoji: string;
  accent: string;
  tagline: string;
  level: string;
  price: number; // цена полного доступа, ₽
  oldPrice: number; // зачёркнутая цена для показа выгоды, ₽
  modules: CourseModule[];
}

/** Доступен ли урок бесплатно: первый урок первого раздела — демо. */
export function isLessonFree(course: SuperCourse, lessonId: string): boolean {
  return course.modules[0]?.lessons[0]?.id === lessonId;
}

