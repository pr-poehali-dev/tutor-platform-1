/**
 * Полноценные программы подготовки к ЕГЭ по профильным предметам.
 * Каждая программа = 8 модулей × 5–10 тем × дидактика (теория, разбор, тренажёр, проверка).
 *
 * Эталонная структура, по которой строится индивидуальный курс
 * в зависимости от целевого балла абитуриента.
 */

import { SubjectCode } from "./graduateData";

export interface PrepTopic {
  id: string;
  title: string;
  /** Минимальный балл за задание в КИМ. */
  egeTaskNumbers: number[];
  /** Сложность: 1 — базовая, 2 — средняя, 3 — сложная (часть 2). */
  difficulty: 1 | 2 | 3;
  /** Ориентировочное время в минутах на освоение темы. */
  estimatedMin: number;
  /** Что именно изучает (3–4 буллета). */
  learn: string[];
  /** Типовые ошибки выпускников. */
  pitfalls?: string[];
}

export interface PrepModule {
  id: string;
  title: string;
  emoji: string;
  /** Какие баллы открывает модуль ("базовая часть" / "часть 2" / "топ-вузы"). */
  scoreBand: "base" | "advanced" | "expert";
  topics: PrepTopic[];
}

export interface PrepProgram {
  subject: SubjectCode;
  /** Полное название курса. */
  title: string;
  /** Кому подходит. */
  audience: string;
  /** Сертификация: ФИПИ, кодификатор, спецификатор. */
  fipiCompliant: boolean;
  /** Максимальный балл, на который рассчитан курс. */
  maxScore: number;
  /** Сколько часов всего. */
  totalHours: number;
  /** Структура курса по модулям. */
  modules: PrepModule[];
  /** Что получит ученик в итоге. */
  outcomes: string[];
  /** Внутренние тренажёры на платформе. */
  trainerSlugs?: string[];
}

