// Определяет, выдаётся ли сертификат о прохождении курса.
//
// По законодательству РФ (ФЗ-273 «Об образовании») без образовательной
// лицензии нельзя выдавать документы об образовании государственного образца.
// Поэтому сертификат:
//   1) выдаётся ТОЛЬКО для курсов вне школьной программы (доп. развитие,
//      профессиональные и гибкие навыки) — где это просветительский/
//      информационный продукт, а не освоение ФГОС;
//   2) носит характер «сертификата о прохождении курса» и НЕ является
//      документом об образовании или о квалификации.

// Школьные предметы (входят в ФГОС / основное общее и среднее образование).
// Для них сертификат НЕ выдаётся — это часть школьной программы.
export const SCHOOL_SUBJECTS: ReadonlySet<string> = new Set([
  "math",
  "physics",
  "chemistry",
  "biology",
  "history",
  "russian",
  "literature",
  "geography",
  "society",
  "english",
]);

// Юридическая оговорка, обязательная на бланке сертификата.
export const CERTIFICATE_DISCLAIMER =
  "Настоящий сертификат подтверждает факт прохождения онлайн-курса и не является документом об образовании или о квалификации (ст. 60 ФЗ № 273-ФЗ «Об образовании в РФ»). Не даёт права на ведение профессиональной деятельности.";

// Короткая версия оговорки для компактных мест.
export const CERTIFICATE_DISCLAIMER_SHORT =
  "Не является документом об образовании или о квалификации.";

// Выдаётся ли сертификат для данного предмета.
export function isCertificateEligible(subject?: string | null): boolean {
  if (!subject) return false;
  return !SCHOOL_SUBJECTS.has(subject.trim().toLowerCase());
}
