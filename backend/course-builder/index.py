# Запрещённый контент по 149-ФЗ, 436-ФЗ, 479-ФЗ. Если встречается в сгенерированной
# программе — курс блокируется до ручной проверки модератором.
FORBIDDEN_PATTERNS = [
    # Экстремизм, терроризм, насилие
    r'\b(экстремизм|террор[иа]|джихад|нацизм|фашизм)\b',
    r'\b(как сделать (бомбу|взрывчатк|оружие))\b',
    r'\b(вербовк[аи]|радикализ[ауц])\b',
    # Наркотики, оружие (изготовление)
    r'\b(синтез|изготовление|производство) (наркотик|психотропн)',
    r'\b(как (приготовить|сварить) (метамфетамин|героин|кокаин))\b',
    # ЛГБТ-пропаганда среди несовершеннолетних (479-ФЗ)
    r'\b(пропаганда (нетрадиционн|лгбт|гей|трансгендерн))\b',
    # Призывы к суициду, селфхарм
    r'\b(способы суицида|как покончить с собой|инструкция по самоубийству)\b',
    r'\b(пропаганда суицида|романтизация суицида)\b',
    # Азартные игры для детей
    r'\b(казино|букмекер|ставк[аи] на спорт)\b.{0,40}\b(дет|школьник|подросток)\b',
    # Обход законов
    r'\b(как (обмануть|обойти) (налогову|закон|инспекци))\b',
    r'\b(отмывание (денег|средств))\b',
    r'\b(фиктивн[ыа][хй] (договор|сделк))\b',
    # Пиратство
    r'\b(пиратск[иая]й? (контент|скачать|раздач))\b',
    r'\b(взлом|кряк) (windows|adobe|microsoft|office)\b',
]


def check_compliance(plan, course_info):
    """Проверяет программу на соответствие законам РФ.
    Возвращает dict: {is_safe: bool, violations: [...], age_rating: '0+'|'6+'|'12+'|'16+'|'18+', disclaimers: [...]}.
    Если is_safe=False, курс должен помечаться is_fallback=True (блок продажи).
    """
    import re

    # Собираем весь текст программы для проверки
    text_parts = [
        plan.get('program_description', ''),
        plan.get('target_audience', ''),
        plan.get('methodology', ''),
        plan.get('final_project', ''),
        ' '.join(plan.get('learning_outcomes', []) or []),
    ]
    for mod in plan.get('modules', []) or []:
        text_parts.append(mod.get('module_title', ''))
        text_parts.append(mod.get('module_description', ''))
        for lesson in mod.get('lessons', []) or []:
            text_parts.append(lesson.get('title', ''))
            text_parts.append(lesson.get('summary', ''))

    full_text = ' '.join(t for t in text_parts if t).lower()

    # 1. Проверка на запрещённый контент
    violations = []
    for pattern in FORBIDDEN_PATTERNS:
        match = re.search(pattern, full_text, re.IGNORECASE)
        if match:
            violations.append(f'запрещённый контент: «{match.group(0)[:50]}»')

    # 2. Определяем возрастную маркировку по 436-ФЗ
    grade = (course_info.get('grade') or '').lower()
    subject = (course_info.get('subject') or '').lower()
    if grade in ('1-4',):
        age_rating = '6+'
    elif grade in ('5-9', 'oge'):
        age_rating = '12+'
    elif grade in ('10-11', 'ege'):
        age_rating = '16+'
    elif grade == 'adult' or subject == 'business':
        age_rating = '18+'
    else:
        age_rating = '12+'

    # 3. Подбираем обязательные дисклеймеры по теме
    disclaimers = []
    if subject in ('business',) or 'инвести' in full_text or 'акции' in full_text or 'трейдинг' in full_text:
        disclaimers.append(
            'Материалы курса носят образовательный характер и не являются индивидуальной '
            'инвестиционной рекомендацией. Решения о вложениях вы принимаете самостоятельно '
            'на свой страх и риск.'
        )
    if 'медицин' in full_text or 'здоров' in full_text or subject == 'biology':
        disclaimers.append(
            'Информация о здоровье носит общеобразовательный характер и не заменяет '
            'консультацию врача. При проблемах со здоровьем обращайтесь к специалисту.'
        )
    if 'юридическ' in full_text or 'правов' in full_text or subject == 'business':
        disclaimers.append(
            'Юридическая информация актуальна на момент создания курса. '
            'Перед принятием юридически значимых решений рекомендуем консультацию юриста, '
            'так как законодательство РФ может меняться.'
        )
    if subject == 'business' or grade == 'adult':
        disclaimers.append(
            'Курс предназначен для лиц старше 18 лет. Соблюдайте налоговое и трудовое '
            'законодательство РФ. Все приведённые примеры — для образовательных целей.'
        )

    # Общий дисклеймер о персональных данных (152-ФЗ)
    disclaimers.append(
        'При прохождении курса соблюдается 152-ФЗ «О персональных данных». '
        'Ваши данные не передаются третьим лицам без вашего согласия.'
    )

    return {
        'is_safe': len(violations) == 0,
        'violations': violations,
        'age_rating': age_rating,
        'disclaimers': disclaimers,
    }


def apply_compliance(plan, course_info):
    """Применяет результаты compliance-проверки к плану.
    Добавляет age_rating, disclaimers; при нарушениях блокирует продажу.
    """
    compliance = check_compliance(plan, course_info)
    plan['age_rating'] = compliance['age_rating']
    plan['disclaimers'] = compliance['disclaimers']
    plan['compliance_checked'] = True
    if not compliance['is_safe']:
        # Блокируем курс — нельзя в продажу пока модератор не проверит
        plan['_fallback'] = True
        plan['_ai_error'] = 'compliance: ' + '; '.join(compliance['violations'])
    return plan


def build_fallback_curriculum(course_info):
    """РЕАЛЬНАЯ программа: построена методистом из конкретных тем ФГОС по предметам.
    Используется когда ИИ недоступен — но качество не хуже ИИ-генерации, потому что
    темы — настоящие из школьной программы, не «занятие 1 / занятие 2».
    Помечается is_fallback=False (готов к продаже) если есть детальный план в CURRICULUM_PLANS."""
    title = course_info.get('title', 'Курс')
    subject = course_info.get('subject', '')
    grade = course_info.get('grade', '')
    total_lessons = int(course_info.get('lessons', 32))
    description = course_info.get('description', '')

    # Ключ плана = subject + grade_band (5-9 / 10-11)
    plan_key = f'{subject}_{grade}'
    plan = CURRICULUM_PLANS.get(plan_key) or CURRICULUM_PLANS.get(subject) or build_generic_plan(subject, grade)

    modules_template = plan['modules']
    n_modules = len(modules_template)
    # Равномерное распределение уроков по модулям (без weight, чтобы не падать с KeyError)
    base_lessons_per_module = max(2, total_lessons // n_modules)

    modules = []
    lesson_global = 0
    for m_idx, mod in enumerate(modules_template):
        # Последний модуль добирает остаток, остальные — поровну
        if m_idx == n_modules - 1:
            lessons_in_module = max(2, total_lessons - lesson_global)
        else:
            lessons_in_module = base_lessons_per_module

        topics_pool = mod.get('topics') or [mod.get('title', 'Тема')]
        if not topics_pool:
            topics_pool = ['Тема урока']
        lessons = []
        for li in range(lessons_in_module):
            lesson_global += 1
            if lesson_global > total_lessons:
                break

            # Берём тему из пула циклически если уроков больше чем тем
            topic = topics_pool[li % len(topics_pool)]
            # Если уроков больше тем — добавляем «практику» / «закрепление»
            if li >= len(topics_pool):
                suffix_idx = (li - len(topics_pool)) % 3
                topic = f'{topic}: {"практикум" if suffix_idx == 0 else "разбор задач" if suffix_idx == 1 else "контрольная работа"}'

            # Тип урока: каждый 4-й — практика, последний модуля — контрольная, финал — проект
            is_last_in_module = (li == lessons_in_module - 1)
            is_final = is_last_in_module and m_idx == len(modules_template) - 1
            if is_final:
                lesson_type = 'project'
            elif is_last_in_module:
                lesson_type = 'test'
            elif (li + 1) % 4 == 0:
                lesson_type = 'practice'
            else:
                lesson_type = 'theory'

            mod_title_safe = mod.get('title', f'Модуль {m_idx + 1}')
            try:
                summary = mod.get('summary_template', 'Изучаем тему «{topic}», разбираем примеры, закрепляем на практике').format(topic=topic)
            except (KeyError, IndexError, ValueError):
                summary = f'Изучаем тему «{topic}», разбираем примеры, закрепляем на практике'
            lessons.append({
                'lesson_index': li + 1,
                'title': topic,
                'summary': summary,
                'type': lesson_type,
                'estimated_minutes': 30 if lesson_type == 'theory' else 35 if lesson_type == 'practice' else 45,
                'topics': [topic, mod_title_safe],
                'skills_acquired': mod.get('skills', ['понимание темы', 'применение на практике']),
                'homework_description': (
                    f'Решить 5–7 задач по теме «{topic}» из рабочей тетради'
                    if lesson_type in ('practice', 'theory') and lesson_global % 2 == 0
                    else None
                ),
            })

        modules.append({
            'module_index': m_idx + 1,
            'module_title': mod.get('title', f'Модуль {m_idx + 1}'),
            'module_description': mod.get('description', ''),
            'lessons': lessons,
        })

    return {
        'program_description': plan.get('program_description') or (
            f'Полноценный курс «{title}» из {total_lessons} занятий для {grade}. '
            f'{description[:120]}' if description else
            f'Систематически изучаем все темы школьной программы с упором на практику и подготовку к контрольным.'
        ),
        'target_audience': plan.get('target_audience', f'Школьники {grade}, изучающие {subject}, кому важна структура и регулярность'),
        'prerequisites': plan.get('prerequisites', ['базовые знания по предмету за предыдущий класс']),
        'learning_outcomes': plan.get('learning_outcomes', [
            'Уверенно решает задачи школьной программы',
            'Понимает теорию и связи между темами',
            'Готов к контрольным и итоговой аттестации',
            'Может объяснить решение и аргументировать ответ',
        ]),
        'methodology': plan.get('methodology', 'Mastery Learning: новая тема → разбор примеров → практика → проверка усвоения. Не идём дальше, пока тема не освоена.'),
        'final_project': plan.get('final_project', 'Комплексная контрольная работа по всем темам курса с подробным разбором решений'),
        'estimated_hours': total_lessons * 30 // 60,
        'modules': modules,
        # Помечаем как НЕ-fallback (готов к продаже), если использовали реальный план,
        # а не generic. Generic-программа остаётся как технический fallback.
        '_fallback': plan.get('_generic', False),
    }


