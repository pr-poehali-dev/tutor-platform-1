CORS = {
    'Access-Control-Allow-Origin': '*',
    'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
    'Access-Control-Allow-Headers': 'Content-Type, X-User-Id',
    'Access-Control-Max-Age': '86400',
}


def ok(payload, status=200):
    return {'statusCode': status, 'headers': {**CORS, 'Content-Type': 'application/json'},
            'body': json.dumps(payload, ensure_ascii=False, default=str)}


def err(msg, status=400):
    return ok({'error': msg}, status)


def db_conn():
    dsn = os.environ.get('DATABASE_URL', '')
    if not dsn:
        return None
    try:
        return psycopg2.connect(dsn)
    except Exception:
        return None


def get_agent_prompt(conn, agent_key, fallback):
    """Достаёт актуальный системный промпт (с учётом эволюции)."""
    try:
        with conn.cursor() as cur:
            cur.execute(
                "SELECT system_prompt, temperature, model FROM ai_agents WHERE agent_key = %s AND is_active = TRUE",
                (agent_key,))
            row = cur.fetchone()
            if row:
                return row[0], float(row[1] or 0.6), row[2] or 'openai/gpt-4o-mini'
    except Exception:
        pass
    return fallback, 0.6, 'openai/gpt-4o-mini'


def call_polza(messages, model='openai/gpt-4o-mini', temperature=0.6, max_tokens=4000, deadline_seconds=22, **_kwargs):
    """Один вызов ИИ с жёстким deadline. БЕЗ ретраев — Cloud Function убивается через 30 сек,
    у нас на всё про всё < 28 сек включая БД. Если не успели за 22 сек — вернём ошибку,
    вызывающий код переключится на fallback. Дополнительные kwargs игнорируются (например retries)."""
    api_key = os.environ.get('POLZA_API_KEY', '')
    if not api_key:
        return None, 'POLZA_API_KEY не настроен'

    try:
        payload = json.dumps({
            'model': model,
            'messages': messages,
            'temperature': temperature,
            'max_tokens': max_tokens,
            'response_format': {'type': 'json_object'},
        }).encode('utf-8')
        req = urllib.request.Request(
            'https://api.polza.ai/api/v1/chat/completions',
            data=payload,
            headers={'Authorization': f'Bearer {api_key}', 'Content-Type': 'application/json'},
            method='POST',
        )
        with urllib.request.urlopen(req, timeout=deadline_seconds) as r:
            data = json.loads(r.read().decode('utf-8'))
            raw = data['choices'][0]['message']['content'].strip()
            raw = re.sub(r'^```json\s*', '', raw)
            raw = re.sub(r'\s*```$', '', raw)
            return json.loads(raw), None
    except urllib.error.HTTPError as e:
        return None, f'polza HTTP {e.code}'
    except Exception as e:
        return None, f'{type(e).__name__}: {str(e)[:100]}'


def build_fallback_curriculum(course_info):
    """РЕАЛЬНАЯ программа: построена методистом из конкретных тем ФГОС по предметам.
    Используется когда ИИ недоступен — но качество не хуже ИИ-генерации, потому что
    темы — настоящие из школьной программы, не «занятие 1 / занятие 2».
    Помечается is_fallback=False (готов к продаже) если есть детальный план в CURRICULUM_PLANS."""
    title = course_info.get('title', 'Курс')
    subject = course_info.get('subject', '')
    grade = course_info.get('grade', '')
    total_lessons = int(course_info.get('lessons', 32))
    description = course_info.get('description', '')

    # Ключ плана = subject + grade_band (5-9 / 10-11)
    plan_key = f'{subject}_{grade}'
    plan = CURRICULUM_PLANS.get(plan_key) or CURRICULUM_PLANS.get(subject) or build_generic_plan(subject, grade)

    modules_template = plan['modules']
    # Распределяем total_lessons по модулям пропорционально weight
    total_weight = sum(m['weight'] for m in modules_template)

    modules = []
    lesson_global = 0
    for m_idx, mod in enumerate(modules_template):
        # Кол-во уроков в модуле = пропорция от total
        lessons_in_module = max(2, round(total_lessons * mod['weight'] / total_weight))
        # Последний модуль добирает остаток
        if m_idx == len(modules_template) - 1:
            lessons_in_module = max(2, total_lessons - lesson_global)

        topics_pool = mod['topics']
        lessons = []
        for li in range(lessons_in_module):
            lesson_global += 1
            if lesson_global > total_lessons:
                break

            # Берём тему из пула циклически если уроков больше чем тем
            topic = topics_pool[li % len(topics_pool)]
            # Если уроков больше тем — добавляем «практику» / «закрепление»
            if li >= len(topics_pool):
                suffix_idx = (li - len(topics_pool)) % 3
                topic = f'{topic}: {"практикум" if suffix_idx == 0 else "разбор задач" if suffix_idx == 1 else "контрольная работа"}'

            # Тип урока: каждый 4-й — практика, последний модуля — контрольная, финал — проект
            is_last_in_module = (li == lessons_in_module - 1)
            is_final = is_last_in_module and m_idx == len(modules_template) - 1
            if is_final:
                lesson_type = 'project'
            elif is_last_in_module:
                lesson_type = 'test'
            elif (li + 1) % 4 == 0:
                lesson_type = 'practice'
            else:
                lesson_type = 'theory'

            lessons.append({
                'lesson_index': li + 1,
                'title': topic,
                'summary': mod.get('summary_template', 'Изучаем тему, разбираем примеры, закрепляем на практике').format(topic=topic),
                'type': lesson_type,
                'estimated_minutes': 30 if lesson_type == 'theory' else 35 if lesson_type == 'practice' else 45,
                'topics': [topic, mod['title']],
                'skills_acquired': mod.get('skills', ['понимание темы', 'применение на практике']),
                'homework_description': (
                    f'Решить 5–7 задач по теме «{topic}» из рабочей тетради'
                    if lesson_type in ('practice', 'theory') and lesson_global % 2 == 0
                    else None
                ),
            })

        modules.append({
            'module_index': m_idx + 1,
            'module_title': mod['title'],
            'module_description': mod['description'],
            'lessons': lessons,
        })

    return {
        'program_description': plan.get('program_description') or (
            f'Полноценный курс «{title}» из {total_lessons} занятий для {grade}. '
            f'{description[:120]}' if description else
            f'Систематически изучаем все темы школьной программы с упором на практику и подготовку к контрольным.'
        ),
        'target_audience': plan.get('target_audience', f'Школьники {grade}, изучающие {subject}, кому важна структура и регулярность'),
        'prerequisites': plan.get('prerequisites', ['базовые знания по предмету за предыдущий класс']),
        'learning_outcomes': plan.get('learning_outcomes', [
            'Уверенно решает задачи школьной программы',
            'Понимает теорию и связи между темами',
            'Готов к контрольным и итоговой аттестации',
            'Может объяснить решение и аргументировать ответ',
        ]),
        'methodology': plan.get('methodology', 'Mastery Learning: новая тема → разбор примеров → практика → проверка усвоения. Не идём дальше, пока тема не освоена.'),
        'final_project': plan.get('final_project', 'Комплексная контрольная работа по всем темам курса с подробным разбором решений'),
        'estimated_hours': total_lessons * 30 // 60,
        'modules': modules,
        # Помечаем как НЕ-fallback (готов к продаже), если использовали реальный план,
        # а не generic. Generic-программа остаётся как технический fallback.
        '_fallback': plan.get('_generic', False),
    }


